package com.company.week3;

public class AnotherOperators {

    public static void main(String[] args) {
        int a = 1;
        ++a;
        System.out.println(a);
        a++;
        System.out.println(a);
        --a;
        System.out.println(a);
        a--;
        System.out.println(a);
    }

}
